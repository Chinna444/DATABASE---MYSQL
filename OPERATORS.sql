  ----  ARITHMETIC OPERATORS ----
SELECT ID,I_NAME,(PRESENT_DAYS+0) AS ADDITION FROM INTERNS_DETAILS
SELECT ID,I_NAME,(30-PRESENT_DAYS) AS SUBTRACT FROM INTERNS_DETAILS
SELECT ID,I_NAME,(ABSENT_DAYS * 250) AS MULTIPLY FROM INTERNS_DETAILS
SELECT ID,I_NAME,(SALARY/PRESENT_DAYS) AS DIVISION FROM INTERNS_DETAILS
SELECT ID,I_NAME,(SALARY%PRESENT_DAYS) AS MODULO FROM INTERNS_DETAILS

---- COMPARISON OPERATORS ----
SELECT * FROM INTERNS_DETAILS WHERE SALARY>20000;
SELECT * FROM INTERNS_DETAILS WHERE SALARY<20000;
SELECT * FROM INTERNS_DETAILS WHERE SALARY=30000;
SELECT * FROM INTERNS_DETAILS WHERE SALARY>=15000;
SELECT * FROM INTERNS_DETAILS WHERE SALARY<=20000;
SELECT * FROM INTERNS_DETAILS WHERE SALARY!=15000;

---- NULL OPERATORS ----
SELECT * FROM INTERNS_DETAILS WHERE ABSENT_DAYS IS NOT NULL;
SELECT * FROM INTERNS_DETAILS WHERE ABSENT_DAYS IS NULL;

---- LOGICAL OPERATORS ----
SELECT * FROM INTERNS_DETAILS WHERE SALARY>20000 AND PRESENT_DAYS>20
SELECT * FROM INTERNS_DETAILS WHERE SALARY<20000 OR  PRESENT_DAYS>26
SELECT * FROM INTERNS_DETAILS WHERE NOT SALARY=20000; 


SELECT * FROM INTERNS_DETAILS WHERE SALARY BETWEEN 20000 AND 30000;
SELECT * FROM INTERNS_DETAILS WHERE SALARY IN (15000,40000);
